# Loops
# Repeat a block of code multiple times.

# Print Hello world 10 times

# for loop
for i in range(10): # 0 to 9
    print(i)

# range(start, stop, step)

print("------------------------------")
for i in range(3, 5):
    print(i)

print("------------------------------")
for i in range(1, 10, 2):
    print(i)